#!/bin/bash

set -o errexit

DOCKER_VERSION=${1-1.0.0}
DOCKER_FILE=Dockerfile

DOCKER_REGISTRY=$DOCKERHUB_REGISTRY
DOCKER_USERNAME=$DOCKERHUB_USERNAME
DOCKER_REPO=$DOCKERHUB_REPOSITORY
DOCKER_IMAGE=$DOCKER_REGISTRY/$DOCKER_USERNAME/$DOCKER_REPO:$DOCKER_VERSION

sed -i "s/WEBAPP_VERSION=.*/WEBAPP_VERSION=$DOCKER_VERSION/g" $DOCKER_FILE

echo docker build -t $DOCKER_IMAGE -f $DOCKER_FILE .
echo docker push $DOCKER_IMAGE

exit 0
